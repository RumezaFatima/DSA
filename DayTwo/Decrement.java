package DayTwo;

public class Decrement {
    public static void main(String[] args) {
        int a = 11;
        int b = --a;
        System.out.println(a);
        System.out.println(b);

        int c = 12;
        int d = c--;
        System.out.println(c);
        System.out.println(d);
    }
}
