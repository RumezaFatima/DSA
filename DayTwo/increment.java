package DayTwo;

public class increment {
    public static void main(String[] args) {
        int a = 12;
        int b = ++a;
        System.out.println(a);
        System.out.println(b);

        int c = 14;
        int d = c++;
        System.out.println(c);
        System.out.println(d);
    }

}
